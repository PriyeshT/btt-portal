export function getSignsByCategory(categoryId: string): Sign[] {
  return SIGNS.filter(s => s.category === categoryId)
}

export function getCategory(id: string): Category | undefined {
  return CATEGORIES.find(c => c.id === id)
}

export function getSignById(id: string): Sign | undefined {
  return SIGNS.find(s => s.id === id)
}

export function getAllFlashcardSigns(): Sign[] {
  // Only return signs that have actual sign images (not road diagrams/markings)
  const flashcardCategories = ['mandatory', 'prohibitory', 'warning', 'regulatory', 'information', 'pedestrian', 'tunnel', 'pedal-cycle', 'emas']
  return SIGNS.filter(s => flashcardCategories.includes(s.category))
}
